import math

print("ADDITION")
a = float(input("Enter first number : "))
b = float(input("Enter second number : "))
print(f"Sum of {a} and {b} is : ", a+b)

print("SUBTRACTION")
c = float(input("Enter first number : "))
d = float(input("Enter second number : "))
print(f"Difference of {c} and {d} is : ", c-d)

print("MULTIPLICATION")
e = float(input("Enter first number : "))
f = float(input("Enter second number : "))
print(f"Product of {e} and {f} is : ", e*f)

print("DIVISION")
g = float(input("Enter first number : "))
h = float(input("Enter second number : "))
print(f"Division of {g} and {h} is : ", g/h)

print("ROOT")
i = float(input("Enter number : "))
print(f"Root of {i} is : ", math.sqrt(i))

print("POWER")
k = float(input("Enter first number : "))
l = float(input("Enter second number : "))
print(f"Power of number {k} of power {l} is : ", k**l)